package model;

/**
 * RoomType.java
 * This enum represents the types of rooms available in the hotel reservation system.
 * The available room types are:
 * - SINGLE: Represents a room with a single bed, typically for one person.
 * - DOUBLE: Represents a room with a double bed, typically for two people.
 */
public enum RoomType {
    SINGLE, // Single room type (for one person)
    DOUBLE  // Double room type (for two people)
}
